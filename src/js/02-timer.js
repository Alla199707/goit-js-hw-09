const input = document.getElementById('datetime-picker');

const datetimePicker = document.querySelector('.timer');
const startBtn = document.querySelector('button[data-start]');
const seconds = document.querySelector('span[data-seconds]');
const minutes = document.querySelector('span[data-minutes]');
const hours = document.querySelector('span[data-hours]');
const days = document.querySelector('span[data-days]');
// startBtn.disabled = true;

startBtn.addEventListener('click', timerStart);

const options = {
  enableTime: true,
  time_24hr: true,
  defaultDate: new Date(),
  minuteIncrement: 1,
  onClose(selectedDates) {
    if (selectedDates[0] < new Date()) {
      //startBtn.disabled = true;
      Notify.failure('Пожалуйста, выберите дату будущего периода');
    } else {
      startBtn.disabled = false;
      timerStart();
      console.log(selectedDates[0]);
      //   console.log(defaultDate);
    }
  },
};

flatpickr(input, options);

function convertMs(ms) {
  const second = 1000;
  const minute = second * 60;
  const hour = minute * 60;
  const day = hour * 24;

  const days = Math.floor(ms / day);
  const hours = Math.floor((ms % day) / hour);
  const minutes = Math.floor(((ms % day) % hour) / minute);
  const seconds = Math.floor((((ms % day) % hour) % minute) / second);

  return { days, hours, minutes, seconds };
}

function addLeadingZero(value) {
  return value.toString().padStart(2, '0');
}

function timerStart() {
  let timer = setInterval(() => {
    let countdown = new Date(input.value) - new Date();
    startBtn.disabled = true;
    datetimePicker.disabled = true;
    if (countdown >= 0) {
      let timeObject = convertMs(countdown);
      days.textContent = addLeadingZero(timeObject.days);
      hours.textContent = addLeadingZero(timeObject.hours);
      minutes.textContent = addLeadingZero(timeObject.minutes);
      seconds.textContent = addLeadingZero(timeObject.seconds);
      if (countdown <= 10000) {
        datetimePicker.style.color = 'red';
      }
    } else {
      datetimePicker.style.color = 'black';
      clearInterval(timer);
    }
  }, 1000);
}

